    private void tabUsuarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabUsuarioMouseClicked
        // TODO add your handling code here:
        int linha = tabUsuario.getSelectedRow();
        String valor = String.valueOf(tabUsuario.getValueAt(linha, 0));
        int codUsuario = Integer.valueOf(valor);
        usuDao.consultarUsuarioID(codUsuario, txt_id, txt_login, cmb_tipo, this);
        btn_alterar.setEnabled(true);
        btn_apagar.setEnabled(true);
        btn_adicionar.setEnabled(false);
    }//GEN-LAST:event_tabUsuarioMouseClicked

