    private void btn_alterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_alterarActionPerformed
        // ALTERAR USUÁRIO
        flag=1;
        btn_alterar.setEnabled(false);
        btn_apagar.setEnabled(false);
        btn_salvar.setEnabled(false);
    }//GEN-LAST:event_btn_alterarActionPerformed

    private void btn_salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_salvarActionPerformed
        // SALVAR USUÁRIO
        
        usuario.setUsu_login(txt_login.getText());
        usuario.setUsu_senha(txt_senha.getText());
        usuario.setUsu_tipo(cmb_tipo.getSelectedIndex());
        
        if(flag==0){
            usuDao.salvarUsuario(usuario, this);
        }else{
            usuario.setUsu_id(Integer.valueOf(txt_id.getText()));
            usuDao.alterarUsuario(usuario, this);
        }
        btn_alterar.setEnabled(false);
        btn_apagar.setEnabled(false);
        btn_salvar.setEnabled(false);
    }//GEN-LAST:event_btn_salvarActionPerformed

    private void btn_apagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_apagarActionPerformed
        // APAGAR
        usuario.setUsu_id(Integer.valueOf(txt_id.getText()));
        usuDao.apagarUsuario(usuario, this);
        btn_alterar.setEnabled(false);
        btn_apagar.setEnabled(false);
        btn_salvar.setEnabled(false);
    }//GEN-LAST:event_btn_apagarActionPerformed

